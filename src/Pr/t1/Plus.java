package Pr.t1;

public class Plus {
    public static void main(String[] args) {
        int a = 0;
        System.out.println(a++);
        System.out.println(a);
        System.out.println("------");
        System.out.println(++a);
        System.out.println(a);
        System.out.println("------");
        System.out.println(--a);
        System.out.println(a);
        System.out.println("------");
        System.out.println(a--);
        System.out.println(a);
        System.out.println("------");
    }
}
