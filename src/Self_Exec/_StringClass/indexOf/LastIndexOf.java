package Self_Exec._StringClass.indexOf;

public class LastIndexOf {
    public static void main(String[] args) {
        String str1 = "abc";
        System.out.println(str1.lastIndexOf("b")); // 1
        System.out.println(str1.lastIndexOf("d")); // -1

        String str2 = "Hello World";
        System.out.println(str2.lastIndexOf("l"));// 9
    }
}
